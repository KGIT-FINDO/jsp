public class ReplyVO {
    int reply_no;
    String reply_name;
    String reply_title;
    String reply_cont;
    int board_ref;
    String reply_step;
    String reply_level;
    String reply_date;

    public int getReply_no() {
        return reply_no;
    }

    public void setReply_no(int reply_no) {
        this.reply_no = reply_no;
    }

    public String getReply_name() {
        return reply_name;
    }

    public void setReply_name(String reply_name) {
        this.reply_name = reply_name;
    }

    public String getReply_title() {
        return reply_title;
    }

    public void setReply_title(String reply_title) {
        this.reply_title = reply_title;
    }

    public String getReply_cont() {
        return reply_cont;
    }

    public void setReply_cont(String reply_cont) {
        this.reply_cont = reply_cont;
    }

    public int getBoard_ref() {
        return board_ref;
    }

    public void setBoard_ref(int board_ref) {
        this.board_ref = board_ref;
    }

    public String getReply_step() {
        return reply_step;
    }

    public void setReply_step(String reply_step) {
        this.reply_step = reply_step;
    }

    public String getReply_level() {
        return reply_level;
    }

    public void setReply_level(String reply_level) {
        this.reply_level = reply_level;
    }

    public String getReply_date() {
        return reply_date;
    }

    public void setReply_date(String reply_date) {
        this.reply_date = reply_date;
    }
}
